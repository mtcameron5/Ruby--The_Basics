# Question: What is exception handling and what problem does it solve.

# Answer: Exception handling is the process of using the begin/rescue/end syntax to catch exceptions. 
# If an exception is caught the program will go to the rescue portion of the begin/rescue/end syntax 
# and run that part of the program


# In other words -- It is a way of handling the error by changing the flow of control (using begin/rescue/end) 
# without exiting the program entirely

